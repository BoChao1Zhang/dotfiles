[ ! -f "$HOME/.local/bin/env" ] || . "$HOME/.local/bin/env"}}egm;
    s{^proxy_on$}{q{if [[ "${DOTFILES_AUTO_PROXY:-0}" == "1" ]]; then
  proxy_on
