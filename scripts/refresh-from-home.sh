    mkdir -p ${ZIM_HOME} && wget -nv -O ${ZIM_HOME}/zimfw.zsh
        https://github.com/zimfw/zimfw/releases/latest/download/zimfw.zsh || true}}egm;
    s{if \[\[ ! \$\{ZIM_HOME\}/init\.zsh -nt \$\{ZIM_CONFIG_FILE:-\$\{ZDOTDIR:-\$\{HOME\}\}/\.zimrc\} \]\]; then\n  source \$\{ZIM_HOME\}/zimfw\.zsh init\nfi}{q{if [[ -r ${ZIM_HOME}/zimfw.zsh && ! ${ZIM_HOME}/init.zsh -nt ${ZIM_CONFIG_FILE:-${ZDOTDIR:-${HOME}}/.zimrc} ]]; then
  source ${ZIM_HOME}/zimfw.zsh init || true
fi}}eg;
    s{^source \$\{ZIM_HOME\}/init\.zsh$}{q{[[ ! -r ${ZIM_HOME}/init.zsh ]] || source ${ZIM_HOME}/init.zsh}}egm;
  ' "$dst"

  if ! grep -Fq '_dotfiles_prepend_fpath_dir()' "$dst"; then
    perl -0pi -e 's{(\n# --------------------\n# Module configuration)}{q{

#
# User-local toolchain
#

[ ! -f "$HOME/.config/dotfiles/shell-env.sh" ] || . "$HOME/.config/dotfiles/shell-env.sh"

_dotfiles_prepend_fpath_dir() {
  local dir="$1"
  [[ -d "$dir" ]] || return
  fpath=("$dir" ${fpath:#"$dir"})
}

for _dotfiles_zsh_prefix in \
  "${DOTFILES_TOOL_PREFIX:-}" \
  "${CONDA_PREFIX:-}" \
  "$HOME/.local/share/dotfiles/toolchain"; do
  [[ -n "$_dotfiles_zsh_prefix" ]] || continue
  _dotfiles_prepend_fpath_dir "$_dotfiles_zsh_prefix/share/zsh/site-functions"
  _dotfiles_prepend_fpath_dir "$_dotfiles_zsh_prefix/share/zsh/functions"
  _dotfiles_prepend_fpath_dir "$_dotfiles_zsh_prefix/share/zsh/$ZSH_VERSION/functions"
  for _dotfiles_zsh_dir in \
    "$_dotfiles_zsh_prefix"/share/zsh/functions/*(N-/) \
    "$_dotfiles_zsh_prefix"/share/zsh/$ZSH_VERSION/functions/*(N-/) \
    "$_dotfiles_zsh_prefix"/share/zsh/*/functions(N-/) \
    "$_dotfiles_zsh_prefix"/share/zsh/*/functions/*(N-/); do
    _dotfiles_prepend_fpath_dir "$_dotfiles_zsh_dir"
  done
done
unset _dotfiles_zsh_prefix _dotfiles_zsh_dir
} . $1}e' "$dst"
  fi

  if ! grep -Fq '_dotfiles_repair_zsh_compinit_permissions()' "$dst"; then
    perl -0pi -e 's{(ZIM_HOME=\$\{ZDOTDIR:-\$\{HOME\}\}/\.zim\n)}{$1 . q{
_dotfiles_repair_zsh_compinit_permissions() {
  local dir
  for dir in "$HOME" "$ZIM_HOME" "${DOTFILES_TOOL_PREFIX:-}/share/zsh"; do
    [[ -d "$dir" ]] || continue
    if [[ "$dir" == "$HOME" ]]; then
      command chmod go-w "$dir" 2>/dev/null || true
    else
      command chmod -R go-w "$dir" 2>/dev/null || true
    fi
  done
}
}}e' "$dst"
  fi

  if grep -Fq '_dotfiles_repair_zsh_compinit_permissions()' "$dst" &&
     ! grep -Fq '_dotfiles_repair_zsh_compinit_permissions
# Initialize modules.' "$dst"; then
    perl -0pi -e 's{(\n# Initialize modules\.)}{\n_dotfiles_repair_zsh_compinit_permissions$1}' "$dst"
  fi

  if ! grep -Fq '$HOME/.config/dotfiles/shell-env.sh' "$dst"; then
    {
      printf '\n'
      printf '[ ! -f "$HOME/.config/dotfiles/shell-env.sh" ] || . "$HOME/.config/dotfiles/shell-env.sh"\n'
    } >> "$dst"
  fi
}

ensure_zsh_helpers() {
  local dst="$1"

  if grep -q '^dotup() {' "$dst"; then
    return
  fi

  cat >> "$dst" <<'ZSH_HELPERS_EOF'

_dotfiles_repo() {
  local repo
  for repo in \
    "${DOTFILES_REPO:-}" \
    "$HOME/dotfiles" \
    "$HOME/.local/share/chezmoi" \
    "/Users/macbook/Documents/Codex/2026-05-09/create-dotfile-chezmoi-codex-claude-tmux/dotfiles"; do
    if [ -n "$repo" ] && [ -d "$repo/.git" ] && [ -x "$repo/scripts/bootstrap.sh" ]; then
      printf '%s\n' "$repo"
      return 0
    fi
  done
  printf 'dotfiles repo not found. Set DOTFILES_REPO=/path/to/dotfiles.\n' >&2
  return 1
}

_dotfiles_git_preflight() {
  if ! git config user.name >/dev/null 2>&1 || ! git config user.email >/dev/null 2>&1; then
    cat >&2 <<'EOF'
Git identity is not configured yet.

Run once on this machine:
  git config --global user.name "BoChao1Zhang"
  git config --global user.email "3210191548@qq.com"

EOF
    return 1
  fi

  local remote_url
  remote_url="$(git remote get-url origin 2>/dev/null || true)"
  case "$remote_url" in
    https://github.com/*)
      cat >&2 <<'EOF'
GitHub no longer accepts account passwords for git push.

Use SSH for this repo:
  ./scripts/setup-github-ssh.sh

Then add the printed public key to GitHub SSH keys and rerun dotup.

EOF
      return 1
      ;;
  esac
}

dotup() {
  local repo msg capture_tokens=0
  repo="$(_dotfiles_repo)" || return
  if [ "${1:-}" = "--tokens" ]; then
    capture_tokens=1
    shift
  fi
  msg="${1:-Update dotfiles from $(hostname -s 2>/dev/null || hostname)}"

  (
    cd "$repo" || exit 1
    _dotfiles_git_preflight || exit 1
    if [ "$capture_tokens" = 1 ]; then
      ./scripts/secrets-capture.sh
    fi
    ./scripts/refresh-from-home.sh
    ./scripts/scan-secrets.sh
    git add -A
    if git diff --cached --quiet; then
      printf 'No dotfiles changes to upload.\n'
      exit 0
    fi
    git commit -m "$msg"
    git push
  )
}

dotdown() {
  local repo
  repo="$(_dotfiles_repo)" || return

  (
    cd "$repo" || exit 1
    git pull --ff-only
    if [ -f secrets/secrets.env.age ]; then
      ./scripts/secrets-install.sh
    fi
    ./scripts/bootstrap.sh
    chezmoi --source "$repo" apply
  )
}
ZSH_HELPERS_EOF
}

