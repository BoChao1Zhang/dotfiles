#!/usr/bin/env bash
set -euo pipefail

repo_root="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")/.." && pwd)"
src_home="${HOME:?HOME is not set}"

need() {
  command -v "$1" >/dev/null 2>&1 || {
    printf 'Missing required command: %s\n' "$1" >&2
    exit 1
  }
}

copy_file() {
  local src="$1"
  local dst="$2"
  [[ -f "$src" ]] || return 0
  mkdir -p -- "$(dirname -- "$dst")"
  cp -p -- "$src" "$dst"
}

copy_tree() {
  local src="$1"
  local dst="$2"
  shift 2
  [[ -d "$src" ]] || return 0
  mkdir -p -- "$dst"
  rsync -a --delete --delete-excluded "$@" "$src"/ "$dst"/
}

remove_tree_symlinks() {
  local dst="$1"
  [[ -d "$dst" ]] || return 0
  find "$dst" -type l -delete
}

sanitize_zprofile() {
  local src="$src_home/.zprofile"
  local dst="$repo_root/dot_zprofile"
  [[ -f "$src" ]] || return 0
  copy_file "$src" "$dst"

  perl -0pi -e '
    s{^eval "\$\(/opt/homebrew/bin/brew shellenv\)"$}{q{if [[ -x /opt/homebrew/bin/brew ]]; then
  eval "$(/opt/homebrew/bin/brew shellenv)"
elif [[ -x /usr/local/bin/brew ]]; then
  eval "$(/usr/local/bin/brew shellenv)"
fi}}egm;
  ' "$dst"

  if ! grep -Fq '$HOME/.config/dotfiles/shell-env.sh' "$dst"; then
    {
      printf '\n'
      printf '[ ! -f "$HOME/.config/dotfiles/shell-env.sh" ] || . "$HOME/.config/dotfiles/shell-env.sh"\n'
    } >> "$dst"
  fi
}

sanitize_zshrc() {
  local src="$src_home/.zshrc"
  local dst="$repo_root/private_dot_zshrc.tmpl"
  [[ -f "$src" ]] || return 0
  perl -0pe '
    s/# Mistral OCR.*?export JINA_API_KEY="[^\n]*"/# Private workflow tokens. This file is generated by scripts\/secrets-install.sh.\n[ ! -f "$HOME\/.config\/dotfiles\/secrets.env" ] || source "$HOME\/.config\/dotfiles\/secrets.env"/s;
  ' "$src" > "$dst"
  harden_zshrc_template "$dst"
  ensure_zsh_helpers "$dst"
}

