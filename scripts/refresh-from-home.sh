    s{^export PATH="/Applications/Xpra\.app/Contents/MacOS:\$PATH"$}{q{if [[ "$OSTYPE" == darwin* ]]; then
  export PATH="/Applications/Xpra.app/Contents/MacOS:$PATH"
fi}}egm;
    s{curl -fsSL --create-dirs -o \$\{ZIM_HOME\}/zimfw\.zsh\n        https://github\.com/zimfw/zimfw/releases/latest/download/zimfw\.zsh$}{q{curl -fsSL --create-dirs -o ${ZIM_HOME}/zimfw.zsh
